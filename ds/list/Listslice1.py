#slicing with list...

users = ["jay","raj","parth","priya","amit","ajay","raj"]

print(users[0::])
print(users[0:3])
print(users[2:5])  # 2 index 5th index
print(users[0:7:2]) #increment by 2
print(users[1:7:3]) #increment by 3 0 + 3 +3 
print("1::",users[1::])

print(users[-1]) #last element
print(users[-2]) #second last element
print(users[::-1]) #reverse list
print(users[::-2]) #reverse list with increment by 2
print(users[::-3]) #reverse list with increment by 2

